void keyboard_post_init_user(void) {
    // Enable the layer system
    rgblight_layers = my_rgb_layers;
}
// This handles BOTH the Tri-Layer logic AND the RGB Colors
layer_state_t layer_state_set_user(layer_state_t state) {
    state = update_tri_layer_state(state, _LOWER, _RAISE, _ADJUST);

    rgblight_set_layer_state(0, get_highest_layer(state) == _QWERTY);
    rgblight_set_layer_state(1, (get_highest_layer(state) == _LOWER || get_highest_layer(state) == _RAISE));
    rgblight_set_layer_state(2, get_highest_layer(state) == _ADJUST);

    return state;

}

